package com.lansun.qmyo.domain;

public class Agenda {

	public int id;
	public String title;
	public String agendaTime;
	public String agendaDate;
	public String alarmTime;
	public String alarmDate;
	public int setAlarm;
	public int typeId;
	public String content;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAgendaTime() {
		return agendaTime;
	}
	public void setAgendaTime(String agendaTime) {
		this.agendaTime = agendaTime;
	}
	public String getAgendaDate() {
		return agendaDate;
	}
	public void setAgendaDate(String agendaDate) {
		this.agendaDate = agendaDate;
	}
	public String getAlarmTime() {
		return alarmTime;
	}
	public void setAlarmTime(String alarmTime) {
		this.alarmTime = alarmTime;
	}
	public String getAlarmDate() {
		return alarmDate;
	}
	public void setAlarmDate(String alarmDate) {
		this.alarmDate = alarmDate;
	}
	public int getSetAlarm() {
		return setAlarm;
	}
	public void setSetAlarm(int setAlarm) {
		this.setAlarm = setAlarm;
	}
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
	
	

}
